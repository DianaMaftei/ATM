
public class NumarComplex {
	private float real;
	private float imaginar;
	
	public NumarComplex(float real, float imaginar) {
		super();
		this.real = real;
		this.imaginar = imaginar;
	}
	public float getReal() {
		return real;
	}
	public float getImaginar() {
		return imaginar;
	}
}
